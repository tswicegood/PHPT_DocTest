<?php

/**
 * This function performs basic subtraction
 *
 * <?php
 *  $result = phpt_subtract(4, 2);
 *  echo $result, "\n";
 * ?>
 * EXPECT:
 * 2
 */
function phpt_subtract($one, $two) {
    return $one - $two;
}

/**
 * This function performs basic multiplication
 *
 * <?php
 *  $result = phpt_multiply(2, 5);
 *  echo $result, "\n";
 * ?>
 * EXPECT:
 * 10
 */
function phpt_multiply($one, $two) {
    return $one * $two;
}

